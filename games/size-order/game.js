function rand(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function shuffle(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = rand(0, i);
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

// ── Question generation ──────────────────────────────────────
function generateQuestion(){
    const labels = ['가','나','다','라'];
    // 4 distinct sizes (px diameters), clearly different
    const baseSizes = [34, 48, 62, 76];
    // assign sizes to the four labels in random order
    const order = shuffle([0,1,2,3].slice()); // order[k] = size-rank index assigned to label k
    // sizeOf[label index] = baseSizes[order[k]]
    const sizeByLabel = order.map(idx => baseSizes[idx]);
    // pick which rank to ask. rank 1 = biggest. easy: rank 1 or 4. normal: rank 2 or 3.
    const isNormal = (typeof difficulty!=='undefined' && difficulty==='normal');
    const rank = isNormal ? (Math.random()<0.5?2:3) : (Math.random()<0.5?1:4);
    // biggest size = 76 (baseSizes[3]); rank 1 -> size index 3, rank 2 -> 2, rank 3 -> 1, rank 4 -> 0
    const wantSizeIdx = 4 - rank; // rank1->3, rank2->2, rank3->1, rank4->0
    const wantSize = baseSizes[wantSizeIdx];
    let answer = '가';
    let cells = '';
    for (let k=0;k<4;k++){
      const sz = sizeByLabel[k];
      if (sz === wantSize) answer = labels[k];
      cells += `<div class="so-item"><span class="so-circle" style="width:${sz}px;height:${sz}px"></span><span class="so-label">${labels[k]}</span></div>`;
    }
    const rankTxt = rank===1 ? '가장 큰' : rank===4 ? '가장 작은' : (rank + '번째로 큰');
    const html = `<div class="so-question">${rankTxt} 동그라미는?</div><div class="so-row">${cells}</div>`;
    const choices = ['가','나','다','라'];
    return { html, answer, choices };
  }

